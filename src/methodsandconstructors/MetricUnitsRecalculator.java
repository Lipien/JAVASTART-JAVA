package methodsandconstructors;

public class MetricUnitsRecalculator {

    double metersToCentimeters(double a) {
        return a * 100;
    }

    double metersToMilimeters(double a) {
        return a * 1000;
    }

    double centimetersToMeters(double a) {
        return a / 100;
    }

    double milimetersToMeters(double a) {
        return a / 100;
    }
}
